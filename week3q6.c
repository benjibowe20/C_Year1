#include <stdio.h>

int main()
{
   int sum1 = 15 + 10;
   printf("%d",sum1);

   int sum2 = 15 - 10;
   printf("\n%d",sum2);

   int sum3 = 15 * 10;
   printf("\n%d",sum3);

   float sum4 = 15 / 10;
   printf("\n%f",sum4);

   int sum5 = 15 % 3;
   printf("\n%d",sum5);



   return 0;
}
